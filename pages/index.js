const prisma = new PrismaClient();

export async function getServerSideProps() {
  const categories = await prisma.category_price.findMany({
    include: {
      flavour: true,
    },
  });

  const clientStories = await prisma.client_stories.findMany({
    where: {
      approved: true,
    },
  });
  return {
    props: {
      categories,
      clientStories
    },
  };
}

